
# string --> int
age = '18'
age_num = int(age)
print('age is %d' % age_num)

# int(x[, base])       将x转换成一个整数
# long(x[, base])      将x转换成一个长整数
# float(x)             将x转换成一个浮点数
# complex(real[, imag])    创建一个复数
# str(x)               将x转换成一个字符串
# repr(x)              将x转换成表达式字符串
# eval(str)            用来计算在字符串中的有效python表达式，并返回一个对象
# tuple(s)             将序列s转换为一个元组
# list(s)              将序列s转换为一个列表
# chr(x)               将一个整数转换为一个字符
# unichr(x)            将一个整数转换为一Unicode字符
# ord(x)               将一个字符转换为它的整数值
# hex(x)               将一个整数转换为一个十六进制字符串
# oct(x)               将一个整数转换为一个八进制字符串